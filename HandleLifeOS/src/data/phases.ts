export function getOverallProgress(): number {
  const total = LIFEOS_PHASES.reduce((sum, p) => sum + p.progress, 0)
  return Math.round(total / LIFEOS_PHASES.length)
}

export function getPhaseSummary() {
  const complete = LIFEOS_PHASES.filter(p => p.status === 'complete').length
  const inProgress = LIFEOS_PHASES.filter(p => p.status === 'in-progress').length
  const planned = LIFEOS_PHASES.filter(p => p.status === 'planned').length
  return { complete, inProgress, planned, total: LIFEOS_PHASES.length }
}
